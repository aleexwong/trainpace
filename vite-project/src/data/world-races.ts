/** Total number of races in the dataset */
export const worldRaceCount = worldRaces.length;

/** Get races by tier */
export function getWorldRacesByTier(tier: RaceTier): WorldRace[] {
  return worldRaces.filter((r) => r.tier === tier);
}

/** Get races by region */
export function getWorldRacesByRegion(region: RaceRegion): WorldRace[] {
  return worldRaces.filter((r) => r.region === region);
}

/** Get races by type */
export function getWorldRacesByType(type: RaceType): WorldRace[] {
  return worldRaces.filter((r) => r.raceType === type);
}

/** Convert races to GeoJSON FeatureCollection for map rendering */
export function racesToGeoJSON(races: WorldRace[]): GeoJSON.FeatureCollection {
  return {
    type: "FeatureCollection",
    features: races.map((race) => ({
      type: "Feature" as const,
      geometry: {
        type: "Point" as const,
        coordinates: [race.lng, race.lat],
      },
      properties: {
        name: race.name,
        slug: race.slug,
        city: race.city,
        country: race.country,
        region: race.region,
        tier: race.tier,
        raceType: race.raceType,
        raceDate: race.raceDate || "",
      },
    })),
  };
}
